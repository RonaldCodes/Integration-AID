        private static void TransferLocationsTosite(Api api, int take, int skip, string toSite, string fromSite)
        {
            var locations = api.ExecuteRequest(new LoadLocations(api.Context, null, take, skip)).Data.Data;
            var api2 = CreateLogin(toSite);

            foreach (var location in locations)
            {
                var coordsCount = location.Coords.Count;
                if (location.Reference != null && coordsCount >= 2)
                {
                    var newLocation = location;
                    newLocation.ClientId = toSite;
                    newLocation.Id = newLocation.Id.Replace($"{fromSite}", $"{toSite}");
                    try
                    {
                        var upload = api2.ExecuteRequest(new SaveLocation(api2.Context, newLocation));
                    }
                    catch(Exception e)
                    {
                        Console.WriteLine($"{e.Message}");
                    }
                    Console.WriteLine($"{location.Reference} Written from {fromSite} to {toSite}...");
                }
            }
        }

